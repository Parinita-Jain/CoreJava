class Variables{
	public static void main(String[] args){
		//Whole Numbers
		byte b = 127;
		short s = 128;
		int i = 8444854;
		long ll = 847678456781L;

		//Decimal Numbers
		float f = 674.45f;
		double d = 4657.434;

		//Character
		char c = 'a';
		char cc = 89;

		//Boolean
		boolean bb = true;

		System.out.println(cc);
	}
}